// lib/models/story_data.dart
import 'package:flutter/material.dart';
class Story {
  final String title;
  final String content;

  Story({required this.title, required this.content});
}

class StoryDataModel with ChangeNotifier {
  final List<Story> _stories = [];

  List<Story> get stories => _stories;

  void addStory(String title, String content) {
    _stories.add(Story(title: title, content: content));
    notifyListeners();
  }

  void removeStory(int index) {
    _stories.removeAt(index);
    notifyListeners();
  }

  void clearStories() {
    _stories.clear();
    notifyListeners();
  }
}

